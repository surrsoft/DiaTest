package ru.surrsoft.baaz.univers;

/**
 * Указывает в каком регистре отображать текст (строку)
 * <p>
 * #version 1 17.12.2016  #id [[w424w]]
 */
public enum ETextCase {
    /**
     * Как есть
     */
    AS_IS,
    /**
     * Все большие
     */
    UC,
    /**
     * Все маленькие
     */
    LC,
    /**
     * Первая буква большая, остальные маленькие
     */
    FUC
}
